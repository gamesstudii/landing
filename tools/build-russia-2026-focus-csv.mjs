function applyChoiceFork({ outcomeId, branch, baseX, laneNames }) {
  const outcome = focuses.find((focus) => focus.id === outcomeId);
  if (outcome) {
    outcome.x = baseX + 1;
    outcome.y = 2;
  }
  const branchFocuses = focuses.filter((focus) => focus.branch === branch && focus.id !== outcomeId);
  if (!branchFocuses.length) return;
  const opener = branchFocuses[0];
  opener.x = baseX + 1;
  opener.y = 3;
  opener.requires = [outcomeId];

  const laneBranches = laneNames.map((name) => `${branch}: ${name}`);
  branchFocuses.slice(1).forEach((focus, index) => {
    const laneIndex = index % laneNames.length;
    const lanePosition = Math.floor(index / laneNames.length);
    focus.branch = laneBranches[laneIndex];
    focus.x = baseX + laneIndex;
    focus.y = 4 + lanePosition;
    const previousLaneFocus = branchFocuses
      .slice(1, index)
      .reverse()
      .find((item) => item.branch === laneBranches[laneIndex]);
    focus.requires = [previousLaneFocus?.id || opener.id];
    if (!previousLaneFocus) {
      focus.blocks = laneBranches.filter((name) => name !== laneBranches[laneIndex]);
    }
  });
}

