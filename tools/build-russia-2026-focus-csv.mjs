function csvEscape(value) {
  const text = value === undefined || value === null ? "" : String(value);
  return /[",\n\r]/.test(text) ? `"${text.replace(/"/g, "\"\"")}"` : text;
}

const childrenById = new Map();
focuses.forEach((focus) => {
  (focus.requires || []).forEach((requiredId) => {
    if (!childrenById.has(requiredId)) childrenById.set(requiredId, []);
    childrenById.get(requiredId).push(focus.id);
  });
});

function detailedText(focus) {
  const branch = focus.branch ? `«${focus.branch}»` : "общего курса";
  const rewards = (focus.reward || []).length
    ? ` Игровой эффект: ${focus.reward.join(", ")}.`
    : "";
  const requirements = (focus.requires || []).length
    ? ` Решение становится доступным после выполнения связанных подготовительных фокусов.`
    : "";
  return `${focus.text}

Подробно: фокус «${focus.name}» разворачивает направление ${branch} через законные парламентские процедуры, бюджетное планирование, публичные программы и контроль исполнения на федеральном и региональном уровнях. В рамках сценария это не меняет конституционные основы напрямую, а оформляет политический мандат как набор управленческих решений, коалиционных договоренностей и административных приоритетов.

Для игрока этот шаг задает более точную специализацию ветки: часть ресурсов переводится в устойчивость системы, часть - в экономические, промышленные или мобилизационные возможности, а часть влияет на темп дальнейших решений. ${requirements}${rewards}`.trim();
}

function rowForFocus(focus) {
  const next = childrenById.get(focus.id) || [];
  const requires = focus.requires || [];
  const reward = focus.reward || [];
  const blocks = focus.blocks || [];
  const random = focus.random || [];

  return [
    focus.id,
    focus.name,
    focus.text,
    focus.details || detailedText(focus),
    focus.days || 70,
    focus.availableFrom || "",
    focus.branch || "",
    focus.x || "",
    focus.y || "",
    ...Array.from({ length: 10 }, (_, index) => next[index] || ""),
    ...Array.from({ length: 10 }, (_, index) => requires[index] || ""),
    ...Array.from({ length: 10 }, (_, index) => reward[index] || ""),
    ...Array.from({ length: 3 }, (_, index) => blocks[index] || ""),
    ...Array.from({ length: 10 }, (_, index) => random[index] || ""),
  ].map(csvEscape).join(",");
}

